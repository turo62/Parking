public class TravelCard {
    private String numberPlate;
    private int hours;
    
    public TravelCard(String numberPlate, int hours) {
        this.numberPlate = numberPlate;
        this.hours = hours;
    }
    
    public String getNumberPlate(String numberPlate) {
        return numberPlate;
    }
    
    public void setHours() {
        this.hours += hours;
    }
    
    public int getHours(int hours) {
        return hours;
    }
}

